function main() {
  console.log(`Running CI result processor in mode: ${mode}`);

  const manifest = loadManifest();
  const results = loadResults();

  if (!results) {
    console.log('‚úÖ No test results found ‚Äî assuming all tests passed');
    process.exit(0);
  }

  const analysis = analyzeResults(results, manifest);
  printSummary(analysis, manifest);

  // Determine exit code based on mode
  let exitCode = 0;

  if (mode === 'critical' || mode === 'all') {
    if (analysis.critical.failed > 0) {
      console.log('‚ùå CI FAILURE: Critical spec failures detected');
      console.log('   ‚Üí Core workflows are broken ‚Äî blocking deployment\n');
      exitCode = 1;
    } else {
      console.log('‚úÖ CI SUCCESS: All critical specs passed');
      console.log('   ‚Üí Core workflows are stable\n');
    }
  }

  if (mode === 'non-critical') {
    if (analysis.nonCritical.failed > 0) {
      console.log('‚ö†Ô∏è  CI WARNING: Non-critical spec failures detected');
      console.log('   ‚Üí Error state tests failing but not blocking\n');
    } else {
      console.log('‚úÖ All non-critical specs passed\n');
    }
  }

  // Unknown specs always exit 0 but warn
  if (analysis.unknown.total > 0) {
    console.log('‚ö†Ô∏è  WARNING: Unclassified specs found ‚Äî update test-manifest.json\n');
  }

  console.log(`Exit code: ${exitCode}\n`);
  process.exit(exitCode);
}

main();
